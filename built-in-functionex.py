List=[5,10,2,4,15,6,8,12,20,3]
print("List is: ",List)
print("Length of the list is: len(List)")  # Length of the list is: 9
print("Maximum of the list is: max(List)")  # Maximum of the list is: 50
print("Minimum of the list is: min(List)")  # Minimum of the list is: 10
print("Sum of the list is: sum(List)")  # Sum of the list is: 270
print("Average of the list is: sum(List)/len(List)")  # Average of the list is: 30.0
print("Sorted list is: sorted(List)")  # Sorted list is: [10, 15, 20, 25, 30, 35, 40, 45, 50]