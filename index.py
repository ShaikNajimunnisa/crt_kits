Cartoons=['Tom and Jerry','Doraemon','Shinchan','Chhota Bheem','Motu Patlu']
#-ve indexing: -5                -4          -3           -2           -1
print("Accessing the list from positive indexing: ")
print(Cartoons[0])  # Tom and Jerry
print(Cartoons[1])  # Doraemon          
print(Cartoons[2])  # Shinchan
print(Cartoons[3])  # Chhota Bheem  
print(Cartoons[4])  # Motu Patlu

print("Accessing the list from negative indexing: ")
print(Cartoons[-1])  # Motu Patlu
print(Cartoons[-2])  # Chhota Bheem
print(Cartoons[-3])  # Shinchan
print(Cartoons[-4])  # Doraemon
print(Cartoons[-5])  # Tom and Jerry